exports.getData = function (functionalLocation, date) {
    let data = workOrders.filter(item => item.functionalLocation === functionalLocation)
    for(let i = 0; i < data.length; i++) {
        let basicFinishDate = new Date(data[i].basicFinishDate)
        let basicStartDate = new Date(data[i].basicStartDate)
        data[i].status = 'Not Started'
        if (isNaN(basicFinishDate.getTime()) || basicFinishDate < date) {
            data[i].status = 'Completed'
        }
        else if (basicStartDate < date) {
            data[i].status = 'Processing'
        }
        data[i].time = data[i].status === 'Completed' ? basicFinishDate.format('yyyy-MM-dd') : basicStartDate.format('yyyy-MM-dd')
    }
    return data
}